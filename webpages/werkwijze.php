            <h3>Broeierij</h3>
            Na het pellen krijgen de bollen een temperatuurbehandeling volgens een strak schema. Zo worden de bollen
            geprepareerd om in de gewenste periodes te groeien.<br><br>
            Vanaf ongeveer half oktober, begin november worden de bollen geplant op kunststof kratten met een soort
            spijkerbedje. Vervolgens gaan deze een speciale hal in om te wortelen. In die hal worden de tulpen in
            meerdere lagen boven elkaar gebroeid. Zo maken we een efficiënt gebruik van de ruimte en wordt er vooral
            zuinig omgegaan met energie.<br><br>
            Na 2 à 3 weken worden de kratten met bollen in de kas geplaatst, zodat er na een maand een mooie bloem op
            zit. Het broeien van de tulpen in de kas gebeurt vanaf december tot en met half juni. Dit gebeurt op een
            systeem met stilstaand water en op een eb-en-vloed systeem.<br><br>
            Als de bloemen de gewenste lengte en gewicht hebben, worden de bloemen geplukt, gebost en op karren in de
            koeling gezet. Uiteindelijk vinden de bloemen via het verpakkingsbedrijf hun weg naar de
            supermarkten.<br><br>
            Zoals gezegd worden de bollen gebroeid op water. Daardoor zijn ze 'schoon' wat de bollen en bloemen ook
            geschikt maakt voor verre en moeilijke afzetmarkten zoals Japan en Amerika waar men schone bloemen eist in
            verband met de fytosanitaire voorschriften. Bovendien heeft het broeien op water als voordeel dat de
            kwaliteit geoptimaliseerd kan worden en de tulpen zich sneller laten verwerken met behoud van
            kwaliteit.<br><br>

            <h3>Verticale bloementeelt</h3>
            In 2011 zijn we gestart met verticale bloementeelt. Daarmee waren we een van de eerste tulpenkwekers in
            Nederland die overging op grootschalige 'interne uitbreiding' met meerlaagsteelt. In deze meerlaags
            oppervlakte van 3000 m2 worden de tulpen vanaf het eerste begin gebroeid in vierlaags containers met een
            eb-en-vloed-systeem. De tulpen blijven de eerste paar dagen in het donker om te ontkiemen, voordat de
            LED-belichting wordt ingezet.<br><br>
            Met het LED-belichtingssysteem kunnen we het spectrum en de intensiteit van het LED-licht afstemmen. Zo
            kunnen we sturen op specifieke groei-eigenschappen van de bloemen. Hiermee hebben we de flexibiliteit om het
            lichtrecept aan te passen gedurende de groeicyclus van de tulpen om tot de perfecte groeibehoeften van de
            tulpen te komen.


